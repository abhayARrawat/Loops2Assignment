#include <bits/stdc++.h>
using namespace std;
int main( ) {
int x = 10, y = 0 ;
while ( x >= y ) {
x-- ;
y++ ;
cout << x << " " << y << endl ;
}
} 
/* loop will iterates 6 time till x>=y when 
x=10 & y=0 --> 9 1 
x=9 & y=1 --> 8 2
x=8 & y=2 --> 7 3
x=7 & y=3 --> 6 4
x=6 & y=4 --> 5 5
x=5 & y=5 --> 4 6
*/